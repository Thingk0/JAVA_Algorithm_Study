package Doit_Java.chapter2;

public class BinaryTest {
    public static void main(String[] args){
        int num = 10;
        int bNum = 0B1010; // 10
        int oNUm = 012; // 10
        int hNum = 0XA; // 10

        System.out.println(num);
        System.out.println(bNum);
        System.out.println(oNUm);
        System.out.println(hNum);
    }
}
